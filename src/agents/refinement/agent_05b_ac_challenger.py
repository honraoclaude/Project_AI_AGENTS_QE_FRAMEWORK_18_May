# ── Agent instructions ────────────────────────────────────────────────────────

_AGENT_INSTRUCTIONS = """
You are the AC Challenger Agent for the FSC Agentic QE Framework.

Your role is adversarial: play the part of a hostile FCA inspector reviewing BDD acceptance
criteria for a Salesforce FSC Wealth Management story. Your goal is to find every weakness
that would allow a defect to reach production undetected.

You are NOT being asked to rewrite the ACs. You are asked to identify specific, concrete
weaknesses. Be precise — cite the exact Given/When/Then step that is weak and explain why.

Weakness types to look for:

1. ambiguous_given
   The Given steps do not specify enough test data state for a QE engineer to reproduce the
   scenario reliably. Examples: field values are unspecified, record relationships are vague,
   a prerequisite Apex class or Flow is referenced but not confirmed active.

2. non_observable_then
   The Then outcomes cannot be verified by an automated test or a manual tester without
   accessing system internals, debug logs, or private Apex state. Examples: "the system
   calculates internally", "a background process runs", "the score is updated eventually".

3. missing_error_path
   An FCA auditor reviewing this story would expect a specific failure mode to be tested
   but it is absent. Examples: missing null-check AC for a required field, no test for
   concurrent update race conditions on a suitability field, no test for governor limit
   breach on a bulk Apex trigger.

4. weak_regulatory
   A regulatory AC clause does not cite the specific FCA rule, COBS section, or Consumer
   Duty outcome it is testing. An auditor cannot map the clause to the obligation without
   additional research. All regulatory clauses must include the specific rule reference.

5. untestable
   The clause is logically vague, contradictory, or cannot be asserted with a pass/fail
   outcome. Examples: "the system behaves correctly", "performance is acceptable",
   "the adviser is satisfied".

Severity guidance:
  CRITICAL : the weakness creates false confidence — a real defect could pass this AC.
  MAJOR    : the weakness significantly reduces test value; fix before sprint start.
  MINOR    : reduces clarity or maintainability; fix before automation.

If a clause has no weaknesses, do NOT include it in challenge_findings.
survivor_count = number of clauses with no CRITICAL or MAJOR weaknesses.

Use the challenge_ac_clauses tool to return your assessment.
""".strip()


# ── Main entry point ──────────────────────────────────────────────────────────

async def run(state: StoryState) -> AgentResult:
    story_id = state["story_id"]

    # Read Agent 5's AC clauses from state
    agent5_result = state["agent_results"].get("5", {})
    agent5_data = agent5_result.get("data", {})
    ac_clauses = agent5_data.get("ac_clauses", [])
    ac_count = len(ac_clauses)
    fca_class = agent5_data.get("fca_classification_context", state.get("fca_classification", "UNCLASSIFIED"))

    if ac_count == 0:
        # Agent 5 produced no clauses — nothing to challenge
        return _no_clauses_result(story_id, fca_class)

    user_message = _build_user_message(story_id, fca_class, ac_clauses)

    extracted = await call_with_tool(
        model=settings.default_model,
        system=build_system(_AGENT_INSTRUCTIONS),
        user_message=user_message,
        tool_name=_TOOL_NAME,
        tool_description=_TOOL_DESCRIPTION,
        tool_schema=_TOOL_SCHEMA,
        max_tokens=2000,
    )

    findings = extracted.get("challenge_findings", [])
    survivor_count = extracted.get("survivor_count", ac_count)
    critical_count = extracted.get("critical_weakness_count", 0)
    summary = extracted.get("challenge_summary", "")

    confidence_score, signals = _compute_confidence(
        ac_count, survivor_count, critical_count, findings
    )
    escalated = confidence_score < settings.confidence_escalation_threshold

    what = (
        f"AC Challenger reviewed {ac_count} clause(s) for {story_id}: "
        f"{survivor_count} survived, {critical_count} critical weakness(es) found"
    )
    why = (
        f"Adversarial AC challenge for a {fca_class}-FCA story. "
        f"{len(findings)} finding(s) across {ac_count} clause(s). "
        f"Survivor rate: {survivor_count}/{ac_count}."
    )

    data = {
        "ac_count_challenged": ac_count,
        "survivor_count": survivor_count,
        "critical_weakness_count": critical_count,
        "challenge_findings": findings,
        "challenge_summary": summary,
        "fca_classification_context": fca_class,
        "signals": signals,
    }

    return AgentResult(
        agent_id=AGENT_ID,
        agent_name=AGENT_NAME,
        what=what,
        why=why,
        data=data,
        confidence=ConfidenceBreakdown(
            tier="B",
            raw_score=confidence_score,
            calibration_multiplier=1.0,
            final_score=confidence_score,
            signals=signals,
            escalated=escalated,
        ),
        model_used=settings.default_model,
    )


# ── Confidence scoring (Tier B) ───────────────────────────────────────────────

def _compute_confidence(
    ac_count: int,
    survivor_count: int,
    critical_count: int,
    findings: list,
) -> tuple[int, dict]:
    scorer = TierBScorer(base=60)

    # Signal 1: clauses to challenge (more clauses = richer basis for assessment)
    if ac_count >= 8:
        scorer.add("rich_ac_set", ac_count, +8)
    elif ac_count >= 4:
        scorer.add("adequate_ac_set", ac_count, +4)
    elif ac_count == 0:
        scorer.add("no_acs_to_challenge", 0, -15)

    # Signal 2: survivor rate (high survival = ACs are strong = challenge is well-grounded)
    if ac_count > 0:
        survival_rate = survivor_count / ac_count
        if survival_rate >= 0.8:
            scorer.add("high_survival_rate", round(survival_rate, 2), +8)
        elif survival_rate >= 0.5:
            scorer.add("moderate_survival_rate", round(survival_rate, 2), +3)
        else:
            scorer.add("low_survival_rate", round(survival_rate, 2), -5)

    # Signal 3: critical weaknesses found (finding CRITICAL issues is the job — reward it)
    if critical_count > 0:
        scorer.add("critical_weaknesses_found", critical_count, +5)

    # Signal 4: finding diversity (multiple weakness types = thorough review)
    weakness_types = {f.get("weakness_type") for f in findings if f.get("weakness_type")}
    if len(weakness_types) >= 3:
        scorer.add("diverse_findings", len(weakness_types), +5)

    scorer.cap(92).floor(20)
    return scorer.build()


# ── Helpers ───────────────────────────────────────────────────────────────────

def _no_clauses_result(story_id: str, fca_class: str) -> AgentResult:
    return AgentResult(
        agent_id=AGENT_ID,
        agent_name=AGENT_NAME,
        what=f"AC Challenger skipped for {story_id}: Agent 5 produced no AC clauses to challenge",
        why="Cannot challenge an empty AC set — Agent 5 must generate clauses first.",
        data={
            "ac_count_challenged": 0,
            "survivor_count": 0,
            "critical_weakness_count": 0,
            "challenge_findings": [],
            "challenge_summary": "No AC clauses available to challenge.",
            "fca_classification_context": fca_class,
            "signals": {"no_acs": True},
        },
        confidence=ConfidenceBreakdown(
            tier="B",
            raw_score=20,
            calibration_multiplier=1.0,
            final_score=20,
            signals={"no_acs": True},
            escalated=True,
        ),
        model_used=settings.default_model,
    )


def _build_user_message(
    story_id: str,
    fca_class: str,
    ac_clauses: list[dict],
) -> str:
    lines = [
        f"Adversarially review the following {len(ac_clauses)} AC clause(s) for story {story_id}.",
        f"FCA Classification: {fca_class}",
        "",
        "AC CLAUSES TO CHALLENGE:",
    ]
    for i, clause in enumerate(ac_clauses):
        lines.append(f"\n[Clause {i}] {clause.get('scenario', 'Unknown scenario')}")
        lines.append(f"  scenario_type : {clause.get('scenario_type', 'unknown')}")
        lines.append(f"  test_category : {clause.get('test_category', 'unknown')}")
        lines.append(f"  fca_relevant  : {clause.get('fca_relevant', False)}")
        for given in clause.get("given", []):
            lines.append(f"  {given}")
        for when in clause.get("when", []):
            lines.append(f"  {when}")
        for then in clause.get("then", []):
            lines.append(f"  {then}")

    lines.append(
        f"\nIdentify every weakness using the {_TOOL_NAME} tool. "
        "Only include clauses that have genuine weaknesses in challenge_findings."
    )
    return "\n".join(lines)
