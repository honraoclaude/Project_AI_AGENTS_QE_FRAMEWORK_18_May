"""
Agent 5B — AC Challenger (Adversarial)
Phase       : Refinement
PACT        : Proactive
Classification: True AI (Sonnet 4.6)
Confidence  : Tier B (base=60)

Runs in Batch 3 of the Refinement phase, in parallel with Agent 6 (Test Design Strategy).
Receives Agent 5's AC clauses from state and plays the adversary role.

Game theory principle — Minimax:
  Agent 5 is the Proposer; Agent 5B is the Challenger.
  ACs that survive adversarial challenge are demonstrably stronger.
  Agent 9 (Risk Anticipation) uses challenge_findings as an additional risk signal.

Purpose:
  Stress-test the acceptance criteria generated by Agent 5 by playing the role
  of a hostile FCA inspector, looking for weaknesses in each BDD clause:
    - Ambiguous Given steps (test data setup cannot be reproduced)
    - Non-observable Then outcomes (cannot assert without internal system access)
    - Missing error paths that an FCA auditor would expect to see tested
    - Weak regulatory scenario coverage (COBS 9.2, Consumer Duty, FG21/1)
    - Untestable clauses (vague, unmeasurable, or contradictory)

Output data keys consumed by downstream:
  challenge_findings      → list  (Agent 9 risk signal)
  survivor_count          → int   (Agent 5 confidence adjustment)
  critical_weakness_count → int   (Gate G1 context, CO email)
  challenge_summary       → str   (CO email, Jira comment)
"""

